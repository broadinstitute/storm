# Get version from Cargo.toml
def get_version():
    cargo_toml = Path(__file__).parent.parent.parent / "Cargo.toml"
    with open(cargo_toml) as f:
        content = f.read()
        match = re.search(r'version\s*=\s*"([^"]+)"', content)
        if match:
            return match.group(1)
        return "0.0.0"  # fallback version

